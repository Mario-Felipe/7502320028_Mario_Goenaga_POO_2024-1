    private void botonActualizarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botonActualizarActionPerformed
        List<Usuario> listado = Principal.usuarioBd.buscarTodo();
        Vector columnas = new Vector();
        columnas.add("Usuario");
        columnas.add("Columna");
        datoTabla = new DefaultTableModel();
        datoTabla.setColumnIdentifiers(columnas);
        
        for (Usuario usuario : listado){
            Vector<String> datos = usuario.convertirAVector();
            datoTabla.addRow(datos);
        }
        
        tablaDatosUsuarios.setModel(datoTabla);
        tablaDatosUsuarios.setRowSelectionInterval(0, 0);
        
        int totalUsuarios = Principal.usuarioBd.contarTodoUsuario();
        etiTotalUsuarios.setText("" + totalUsuarios);
    }//GEN-LAST:event_botonActualizarActionPerformed

